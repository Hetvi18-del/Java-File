class Swap
{
	public static void main(String args[])
	{
		int a=10,b=35,temp;
		System.out.println("the number of a is :" +a);
		System.out.println("the number of b is :" +b);
		temp=a;
		a=b;
		b=temp;
		System.out.println("after swapping");
		System.out.println("the number of a is :" +a);
		System.out.println("the number of b is :" +b);
	}
}